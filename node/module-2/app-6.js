// Using if-else statements:
const age1 = 18;
if (age1 >= 18) {
  console.log("You are an adult.");
} else {
  console.log("You are a minor.");
}

// Using a conditional operator: ternary operator

const age2 = 16;
const message = age2 >= 18 ? "You are an adult." : "You are a minor.";
console.log(message);
